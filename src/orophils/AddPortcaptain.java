    public void loadComboBox(){
    
        Connection myConn = null;
        Statement myStmt = null;
        
        Statement myStmt1 = null;
        
        Statement myStmt2 = null;
        
        ResultSet myRs = null;
        ResultSet rs2 = null;
        ResultSet rs3 = null;

        String user = "root";
        String pass = "root";

        try {
            // 1. Get a connection to database
            
            Class.forName("org.gjt.mm.mysql.Driver");
            myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/orophil", user, pass);

            // 2. Create a statement
            myStmt = myConn.createStatement();
            myStmt1 = myConn.createStatement();
            myStmt2 = myConn.createStatement();

            // 3. Execute SQL query
            myRs = myStmt.executeQuery("select * from employeelist");
            rs2 = myStmt1.executeQuery("select * from documentchecklist");
            rs3 = myStmt2.executeQuery("select * from crewingofficer");
            
            documentID.removeAllItems();
            crewingOfficer.removeAllItems();
            employee.removeAllItems();
            
            documentID.addItem("--NONE-_NULL");
            crewingOfficer.addItem("--NONE-_NULL");
            employee.addItem("--NONE-_NULL");
            
            // 4. Process the result set
            while (myRs.next()) {
                employee.addItem(myRs.getString("LastName")+", "+myRs.getString("FirstName")+"_"+myRs.getInt("EmployeeID"));
            }
            while (rs2.next()) {
                documentID.addItem("_"+rs2.getInt("DocumentID"));
            } 
            while (rs3.next()) {
                crewingOfficer.addItem(rs3.getString("LastName")+", "+rs3.getString("FirstName")+"_"+rs3.getInt("CrewingOfficerID"));
            } 

        } catch (Exception exc) {
            exc.printStackTrace();
        } finally {
        }
    }
