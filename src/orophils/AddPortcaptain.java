    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed

        Connection conn = null;
        Statement myStmt = null;
        ResultSet myRs = null;

        String user = "root";
        String pass = "root";
        String query = "insert into portcaptain (Lastname, Firstname, ConfidentialityReport, CrewingofficerID, DocumentID, EmployeeID)"
        + " values (?, ?, ?, ?, ?,?)";
        try {
            // 1. Get a connection to database
            Class.forName("org.gjt.mm.mysql.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/orophil", user, pass);
            // 2. Create a statement
            myStmt = conn.createStatement();
            // create the mysql insert preparedstatement
            PreparedStatement preparedStmt = conn.prepareStatement(query);
            
            preparedStmt.setString (1, lname.getText());
            preparedStmt.setString (2, fname.getText());
            
                System.out.print(documentID.getSelectedItem().toString().substring(documentID.getSelectedItem().toString().lastIndexOf("_") + 1));
                System.out.print(crewingOfficer.getSelectedItem().toString().substring(crewingOfficer.getSelectedItem().toString().lastIndexOf("_") + 1));
            
            
            if(documentID.getSelectedItem().toString().substring(documentID.getSelectedItem().toString().lastIndexOf("_") + 1).equals("NULL")){
                preparedStmt.setNull (5, Types.INTEGER);
            }else{
                preparedStmt.setInt (5, Integer.parseInt(documentID.getSelectedItem().toString().substring(documentID.getSelectedItem().toString().lastIndexOf("_") + 1)));
            }
            if(crewingOfficer.getSelectedItem().toString().substring(crewingOfficer.getSelectedItem().toString().lastIndexOf("_") + 1).equals("NULL")){
                preparedStmt.setNull (4, Types.INTEGER);
            }else{
                preparedStmt.setInt (4, Integer.parseInt(crewingOfficer.getSelectedItem().toString().substring(crewingOfficer.getSelectedItem().toString().lastIndexOf("_") + 1)));
            }
            if(employee.getSelectedItem().toString().substring(employee.getSelectedItem().toString().lastIndexOf("_") + 1).equals("NULL")){
                preparedStmt.setNull (6, Types.INTEGER);
            }else{
                preparedStmt.setInt (6, Integer.parseInt(employee.getSelectedItem().toString().substring(employee.getSelectedItem().toString().lastIndexOf("_") + 1)));
            }

      
            if(Ttrue.isSelected()){
                preparedStmt.setString (3, "True");
            }
            else if(Ffalse.isSelected()){
                preparedStmt.setString (3, "False");
            }
            // execute the preparedstatement
            preparedStmt.execute();

        } catch (Exception exc) {
            exc.printStackTrace();
        } finally {
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(AddPortcaptain.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(null, "Added Successfully");
        this.dispose();
        //clearEntry();
    }//GEN-LAST:event_submitActionPerformed

